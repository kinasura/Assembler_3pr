; Тестовая программа с отрицательными числами
; Проверяем, что команды работают с отрицательными значениями

; 1. Загружаем отрицательную константу
158,-100,0    ; R0 = -100

; 2. Загружаем положительную константу  
158,200,1     ; R1 = 200

; 3. Загружаем адрес памяти
158,1000,2    ; R2 = 1000

; 4. Записываем отрицательное значение в память
12,2,0        ; memory[1000] = R0 (-100)

; 5. Читаем обратно - должно быть -100
17,1000,3     ; R3 = memory[1000]

; 6. Тестируем ABS с отрицательным числом
214,0,2,0     ; memory[R2+0] = abs(R0) (memory[1000] = 100)

; 7. Тестируем ABS с положительным числом
158,1001,4    ; R4 = 1001
214,0,4,1     ; memory[R4+0] = abs(R1) (memory[1001] = 200)

; 8. Тестируем ABS с отрицательным смещением
158,-50,5     ; R5 = -50
158,1050,6    ; R6 = 1050
214,-25,6,5   ; memory[R6-25] = abs(R5) (memory[1025] = 50)