; Тестовая программа для проверки команды ABS
; Демонстрирует вычисление абсолютного значения с различными входами
; ВСЕ КОНСТАНТЫ ДОЛЖНЫ БЫТЬ В ПРЕДЕЛАХ 30 БИТ (знаковое): от -536870912 до 536870911

; Инициализация базовых адресов
158,1000,0    ; R0 = 1000 (базовый адрес для результатов)

; Тест 1: ABS положительного числа
158,123,1     ; R1 = 123 (положительное число)
214,0,0,1     ; memory[1000] = abs(123) = 123

; Тест 2: ABS отрицательного числа
158,-456,2    ; R2 = -456 (отрицательное число)
214,4,0,2     ; memory[1004] = abs(-456) = 456

; Тест 3: ABS нуля
158,0,3       ; R3 = 0 (ноль)
214,8,0,3     ; memory[1008] = abs(0) = 0

; Тест 4: ABS большого положительного числа (в пределах 30 бит)
158,500000000,4   ; R4 = 500000000 (большое положительное, но в пределах 30 бит)
214,12,0,4    ; memory[1012] = abs(500000000) = 500000000

; Тест 5: ABS большого отрицательного числа (в пределах 30 бит)
158,-500000000,5  ; R5 = -500000000 (большое отрицательное, но в пределах 30 бит)
214,16,0,5    ; memory[1016] = abs(-500000000) = 500000000

; Тест 6: ABS с отрицательным смещением
158,-999,6    ; R6 = -999
158,1100,7    ; R7 = 1100 (базовый адрес)
214,-100,7,6  ; memory[1000] = abs(-999) = 999

; Тест 7: ABS с положительным смещением
158,777,8     ; R8 = 777
158,1200,9    ; R9 = 1200
214,50,9,8    ; memory[1250] = abs(777) = 777

; Завершение программы
; Считываем результаты для проверки
17,1000,10    ; R10 = memory[1000] (должно быть 999 - перезаписали тест 1!)
17,1004,11    ; R11 = memory[1004] (должно быть 456)
17,1008,12    ; R12 = memory[1008] (должно быть 0)
17,1012,13    ; R13 = memory[1012] (должно быть 500000000)
17,1016,14    ; R14 = memory[1016] (должно быть 500000000)
17,1250,15    ; R15 = memory[1250] (должно быть 777)